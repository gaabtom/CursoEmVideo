n1 = int(input('Um valor:'))
n2 = int(input('Outro valor:'))
s = n1 + n2
m = n1 * n2
d = n1 / n2
p = n1 ** n2
dint = n1 // n2
rd = n1 % n2
print(f'A soma vale {s}')
print(f'A multiplicação vale {m}')
print(f'A divisão vale {d:.2f}')
print(f'A potência vale {p}')
print(f'A divisão inteira vale {dint}')
print(f'O resto da divisão vale {rd}')

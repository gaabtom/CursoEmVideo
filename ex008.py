# Desafio 008 - Crie um programa que leia um valor em metros e o converta para centímetros e milímetros.

n1 = float(input('Digite um valor em metros:'))
print(f'Para {n1} metros, temos: \n {n1*1000} milímetros; \n {n1*100} centímetros; \n {n1*10} decímetros; \n {n1/10} decâmetros; \n {n1/100} hectômetros \n {n1/1000} e quilômetros.')

